/**
 * op_add - add 2 integers
 * @a: first int
 * @b: second int
 *
 * Return: summation of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - add 2 integers
 * @a: first int
 * @b: second int
 *
 * Return: difference btw a and b
 */ 
int op_sub(int a, int b)
{
	return (a - b);
} 

/**
 * op_mul - add 2 integers
 * @a: first int
 * @b: second int
 *
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
} 

/**
 * op_div - divide 2 integers
 * @a: first int
 * @b: second int
 *
 * Return: division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - get the modulus between 2 integers
 * @a: first int
 * @b: second int
 *
 * Return: division of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
